public class Order {

	String brothId;
	String proteinId;

	public String getBrothId() {
		return brothId;
	}

	public void setBrothId(String brothId) {
		this.brothId = brothId;
	}

	public String getProteinId() {
		return proteinId;
	}

	public void setProteinId(String proteinId) {
		this.proteinId = proteinId;
	}
}
